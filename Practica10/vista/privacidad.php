    <h3 class="text-center">Modalidad y lugar del tratamiento de los Datos recogidos</h3>
    <p>
        <center><b> Modalidades de Tratamiento</b><br>
            El Titular tratará los Datos de los Usuarios de manera adecuada y adoptará las medidas de seguridad
            apropiadas
            para impedir el acceso, la revelación, alteración o destrucción no autorizados de los Datos. El tratamiento
            de
            Datos se realizará mediante ordenadores y/o herramientas informáticas, siguiendo procedimientos y
            modalidades
            organizativas estrictamente relacionadas con las finalidades señaladas. Además del Titular, en algunos casos
            podrán acceder a los Datos ciertas categorías de personas autorizadas, relacionadas con el funcionamiento de
            esta Aplicación (administración, ventas, marketing, departamento jurídico y de administración de sistemas) o
            contratistas externos que presten servicios al Titular (tales como proveedores externos de servicios
            técnicos,
            empresas de mensajería, empresas de hosting, empresas de informática, agencias de comunicación) que serán
            nombrados por el Titular como Encargados del Tratamiento, si fuera necesario. Se podrá solicitar al Titular
            en
            cualquier momento una lista actualizada de dichas personas.
    </p>

    <p><b> Base jurídica del Tratamiento</b> <br>
        El Titular podrá tratar los Datos Personales del Usuario, si se cumple una de las siguientes condiciones: <br>
        <b>-</b>Cuando los Usuarios hayan dado su consentimiento para una o más finalidades específicas. Aviso: Al
        amparo de
        varias legislaciones diferentes, el Titular podrá estar autorizado a tratar los Datos Personales hasta que el
        Usuario se oponga a ello (“opción de no participación”), sin necesidad de consentimiento o de cualquier otra
        base jurídica. Sin embargo, esto no será aplicable cuando el tratamiento de los Datos Personales esté sujeto a
        la normativa europea en materia de protección de Datos Personales; <br>
        <b>-</b>Cuando la obtención de Datos sea necesaria para el cumplimiento de un contrato entre el Usuario y/o
        cualquier
        otra obligación precontractual del mismo; <br>
        <b>-</b>Cuando el tratamiento sea necesario para el cumplimiento de una obligación legal de obligado
        cumplimiento por
        parte del Usuario; <br>
        <b>-</b>Cuando el tratamiento esté relacionado con una tarea ejecutada en interés público o en el ejercicio de
        competencias oficiales otorgadas al Titular;<br>
        <b>-</b>Cuando el tratamiento sea necesario con el fin de un interés legítimo perseguido por el Titular o un
        tercero.
        <br>
        En cualquier caso, el Titular está a su disposición para definir las bases jurídicas específicas que se aplican
        al tratamiento y en particular, si la obtención de los Datos Personales es un requisito contractual o
        estatutario o un requisito necesario para formalizar un contrato.
    </p>
    <p>
        <b>Lugar</b> <br>
        Los Datos se tratan en las oficinas del Titular, así como en cualquier otro lugar en el que se encuentren
        situadas las partes implicadas en dicho proceso de tratamiento. Dependiendo de la localización de los
        Usuarios,
        las transferencias de Datos pueden implicar la transferencia de los Datos de los Usuarios a otro país
        diferente
        al suyo propio. Para más información sobre el lugar de tratamiento de dichos Datos transferidos, los
        Usuarios
        podrán consultar la sección que contiene los detalles sobre el tratamiento de los Datos Personales. Los
        Usuarios
        también tendrán derecho a conocer las bases legales de las transferencias de Datos a otro país fuera de la
        Unión
        Europea o a cualquier organismo internacional que se rija por el Derecho Público Internacional o que esté
        formado por dos o más países, como la ONU, y además conocer las medidas de seguridad tomadas por el Titular
        para
        salvaguardar sus Datos. En caso de que tuviera lugar dicha transferencia de Datos, los Usuarios podrán
        obtener
        más información consultando las secciones relevantes del presente documento o solicitándola al Titular, a
        través
        de la información de contacto que aparece en la sección de contacto.
    </p>
    <p><b>Período de conservación</b> <br>
        Los Datos Personales serán tratados y conservados durante el tiempo necesario y para la finalidad por la que han
        sido recogidos. Por lo tanto: Los Datos Personales recogidos para la formalización de un contrato entre el
        Titular y el Usuario deberán conservarse como tales hasta en tanto dicho contrato se haya formalizado por
        completo. Los Datos Personales recogidos en legítimo interés del Titular deberán conservarse durante el tiempo
        necesario para cumplir con dicha finalidad. Los Usuarios pueden encontrar información específica relacionada con
        el interés legítimo del Titular consultando las secciones relevantes del presente documento o contactando con el
        Titular. El Titular podrá conservar los Datos Personales durante un periodo adicional cuando el Usuario preste
        su consentimiento a tal tratamiento, siempre que dicho consentimiento siga vigente. Además, el Titular estará
        obligado a conservar Datos Personales durante un periodo adicional siempre que se precise para el cumplimiento
        de una obligación legal o por orden que proceda de la autoridad. Una vez terminado el período de conservación,
        los Datos Personales deberán eliminarse. Por lo tanto, los derechos de acceso, modificación, rectificación y
        portabilidad de datos no podrán ejercerse una vez haya expirado dicho periodo. </p>
    <h3 class="text-center">Finalidad del Tratamiento de los Datos recogidos </h3>
    <p>Los Datos relativos al Usuario son recogidos para permitir al Titular prestar su Servicio, cumplir sus
        obligaciones legales, responder a solicitudes de ejecución, proteger sus derechos e intereses (o los de sus
        Usuarios o terceros), detectar cualquier actividad maliciosa o fraudulenta, así como para las siguientes
        finalidades: Acceso a las cuentas de servicios de terceros y Contactar con el Usuario. Los Usuarios pueden
        encontrar información específica los Datos personales utilizados para cada finalidad en la sección “Información
        detallada del Tratamiento de los Datos Personales”. </p>
